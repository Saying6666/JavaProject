package main.java.com.comment;

/**
 * 注释Demo
 * 目标：认识三种注释
 * 1. 单行注释
 * 2. 多行注释
 * 3. 文档注释
 */
public class commentDemo {
    public static void main(String[] args) {
        //目标认识三种注释
        // 1. 单行注释：以 // 开头，注释内容在同一行
        //打印两行诗句
        System.out.println("床前明月光，");

        // 2. 多行注释：以 /* 开头，以 */ 结尾，可以跨多行
        /*
        月是故乡明。
        这是一段多行注释，注释内容可以跨越多行。


         */

    }
}
